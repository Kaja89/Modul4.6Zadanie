
public class Calculator {
	
	public double add(double a, double b) {
		double result = a + b;
		System.out.println("Sum " + a + " + " + b + " equals: " + result);
		return result;
	}
	
	public double subtract(double a, double b) {
		double result = a - b;
		System.out.println("Sum " + a + " - " + b + " equals: " + result);
		return result;
	}
}
